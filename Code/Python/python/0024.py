a = 50
# ตั้งแต่ 1 ล้านคนขึ้นไป ได้เกรด ssr

# - ตั้งแต่ 1 แสนคนขึ้นไป แต่ไม่ถึง 1 ล้านคน ได้เกรด a

# - ตั้งแต่ 1 หมื่นคนขึ้นไป แต่ไม่ถึง 1 แสนคน ได้เกรด b+

# - ตั้งแต่ 1 พันคนขึ้นไป แต่ไม่ถึง 1 หมื่นคน ได้เกรด b

# - น้อยกว่า 1 พันคน ได้เกรด c
if(a>=1e6): 
    print("ssr")
elif(a>=1e5): 
    print("a")
elif(a>=1e4): 
    print("b+")
elif(a>=1e3): 
    print("b")
else: 
    print("c")